def exact_ground_energy(hamiltonian: Hamiltonian) -> float:
    """Return exact dense-matrix ground energy.

    Warning: dense diagonalization scales as O(2^(3n)); intended for the smaller
    debug cases (<= 12 qubits). For larger sizes use the sparse Lanczos path in
    ``NH3_result_spin.py``.
    """

    backend = NumpyBackend()
    matrix = hamiltonian.to_matrix(backend)
    matrix_np = np.asarray(backend.to_numpy(matrix), dtype=np.complex64)
    return float(np.linalg.eigvalsh(matrix_np).min())


def nh3_vqe_qas_kwargs() -> dict:
    """Recommended starter configuration for 12-qubit NH3 supernet search."""

    n_qubits = 2 * NH3_ACTIVE_SPATIAL_ORBITALS
    hf_qubits, single_excitations, double_excitations = closed_shell_excitation_pools(
        NH3_ACTIVE_ELECTRONS,
        NH3_ACTIVE_SPATIAL_ORBITALS,
    )
    return {
        "n_qubits": n_qubits,
        "layers": 3,
        "single_qubit_gates": ("i",),
        "two_qubit_gates": ("single_excitation",),
        "two_qubit_pairs": single_excitations,
        "four_qubit_gates": ("double_excitation",),
        "four_qubit_groups": double_excitations,
        "hf_occupied_qubits": hf_qubits,
        "supernet_num": 6,
        "supernet_steps": 300,
        "ranking_num": 120,
        "finetune_steps": 500,
        "learning_rate": 0.08,
        "finetune_learning_rate": 0.04,
        "seed": 7,
        "task": "vqe",
    }


def save_circuit_python(
    circuit,
    file_path: Path,
    func_name: str = "build_nh3_qas_circuit",
    figure_name: str = "NH3_cir.png",
    title: str = "NH3 supernet ground-state ansatz",
) -> None:
    source = circuit_to_python_source(
        circuit,
        func_name=func_name,
        figure_name=figure_name,
        title=title,
    )
    source = (
        source.replace("by demos/H2O/H2O.py.", "by demos/NH3/NH3.py.")
        .replace("python -m demos.H2O.H2O``,", "python -m demos.NH3.NH3``,")
        .replace("python -m demos.H2O.H2O_cir``,", "python -m demos.NH3.NH3_cir``,")
        .replace("H2O", "NH3")
        .replace("build_h2o_qas_circuit", "build_nh3_qas_circuit")
    )
    file_path.write_text(source, encoding="utf-8")


def _parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="NH3 12-qubit supernet VQE demo.")
    parser.add_argument("--device", default="cpu", help="torch device (cpu/cuda/npu:0)")
    parser.add_argument("--layers", type=int, default=3)
    parser.add_argument("--supernet-num", type=int, default=6)
    parser.add_argument("--supernet-steps", type=int, default=300)
    parser.add_argument("--ranking-num", type=int, default=120)
    parser.add_argument("--finetune-steps", type=int, default=500)
    parser.add_argument("--seed", type=int, default=7)
    parser.add_argument(
        "--skip-exact",
        action="store_true",
        help="Skip exact dense diagonalization (recommended for >12 qubits).",
    )
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> None:
    args = _parse_args(argv)

    print("NH3 fixed active-space Hamiltonian")
    print("Geometry (angstrom):")
    for atom, coords in NH3_GEOMETRY_ANGSTROM:
        x, y, z = coords
        print(f"  {atom:>2s}: ({x:+.6f}, {y:+.6f}, {z:+.6f})")
    print(f"Basis : {NH3_BASIS}")
    print(f"Charge: {NH3_CHARGE}")
    print(f"Spin  : {NH3_SPIN}")
    print(
        "Active space: "
        f"{NH3_ACTIVE_ELECTRONS} electrons, {NH3_ACTIVE_SPATIAL_ORBITALS} spatial orbitals"
    )
    print(f"Mapper: {NH3_QUBIT_MAPPER}")

    t0 = time.time()
    hamiltonian = build_nh3_hamiltonian()
    t1 = time.time()

    terms = hamiltonian_pauli_terms(hamiltonian)
    print(f"\nQubits: {hamiltonian.n_qubits}")
    print(f"Terms : {len(terms)}")
    print(f"Hamiltonian load time: {t1 - t0:.1f}s")

    if not args.skip_exact and hamiltonian.n_qubits <= 12:
        exact = exact_ground_energy(hamiltonian)
        print(f"Dense-matrix exact ground energy: {exact:+.10f}")
    else:
        exact = None
        print("Dense-matrix exact ground energy: skipped")

    kwargs = nh3_vqe_qas_kwargs()
    kwargs.update(
        {
            "layers": args.layers,
            "supernet_num": args.supernet_num,
            "supernet_steps": args.supernet_steps,
            "ranking_num": args.ranking_num,
            "finetune_steps": args.finetune_steps,
            "seed": args.seed,
            "device": args.device,
        }
    )

    print("\nSearching NH3 ground-state circuit with supernet (task='vqe')...")
    start = time.time()
    result = supernet_qas(hamiltonian, **kwargs)
    elapsed = time.time() - start

    metrics = result.final_metrics
    qas_energy = float(metrics["fine_tuned_energy"])
    baseline_energy = float(metrics["baseline_vqe_energy"])
    print(f"QAS fine-tuned energy     : {qas_energy:+.10f}")
    print(f"fixed-ansatz VQE baseline : {baseline_energy:+.10f}")
    if exact is not None:
        print(f"|QAS - exact|             : {abs(qas_energy - exact):.3e} Ha")
    print(
        "selected excitations      : "
        f"{metrics['selected_excitation_count']} "
        f"(single={metrics['selected_two_qubit_count']}, double={metrics['selected_four_qubit_count']})"
    )
    print(f"wall-clock time           : {elapsed:.1f}s")

    qasm_path = Path(__file__).with_name("NH3_cir.qasm")
    saved_qasm, qasm_message = save_qasm3_if_supported(result.best_circuit, qasm_path)
    if saved_qasm:
        print(f"OpenQASM 3.0 saved to: {qasm_message}")
    else:
        print(f"OpenQASM 3.0 skipped: {qasm_message}")

    py_path = Path(__file__).with_name("NH3_cir.py")
    save_circuit_python(result.best_circuit, py_path)
    print(f"Python circuit saved to: {py_path}")


if __name__ == "__main__":
    main()
