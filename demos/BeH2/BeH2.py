def exact_ground_energy(hamiltonian: Hamiltonian) -> float:
    """Return exact dense-matrix ground energy.

    Warning: dense diagonalization scales as O(2^(3n)); for 16 qubits this is
    usually too expensive, so this function is intended for smaller debug cases.
    """

    backend = NumpyBackend()
    matrix = hamiltonian.to_matrix(backend)
    matrix_np = np.asarray(backend.to_numpy(matrix), dtype=np.complex64)
    return float(np.linalg.eigvalsh(matrix_np).min())


def beh2_vqe_qas_kwargs() -> dict:
    """Recommended starter configuration for 16-qubit BeH2 supernet search."""

    n_qubits = 2 * BEH2_ACTIVE_SPATIAL_ORBITALS
    two_qubit_pairs = tuple(
        (i, j)
        for i in range(n_qubits)
        for j in range(i + 1, n_qubits)
        if j - i <= 1
    )
    return {
        "n_qubits": n_qubits,
        "layers": 4,
        "single_qubit_gates": ("i", "h", "rx", "ry", "rz"),
        "two_qubit_gates": ("cx", "rzz"),
        "two_qubit_pairs": two_qubit_pairs,
        "supernet_num": 6,
        "supernet_steps": 300,
        "ranking_num": 120,
        "finetune_steps": 500,
        "learning_rate": 0.08,
        "finetune_learning_rate": 0.04,
        "seed": 7,
        "task": "vqe",
    }


def save_circuit_python(circuit, file_path: Path, func_name: str = "build_beh2_qas_circuit") -> None:
    source = circuit_to_python_source(
        circuit,
        func_name=func_name,
        figure_name="BeH2_cir.png",
        title="BeH2 supernet ground-state ansatz",
    )
    source = (
        source.replace("by demos/H2O/H2O.py.", "by demos/BeH2/BeH2.py.")
        .replace("python -m demos.H2O.H2O``,", "python -m demos.BeH2.BeH2``,")
        .replace("python -m demos.H2O.H2O_cir``,", "python -m demos.BeH2.BeH2_cir``,")
        .replace("H2O", "BeH2")
        .replace("build_h2o_qas_circuit", "build_beh2_qas_circuit")
    )
    file_path.write_text(source, encoding="utf-8")


def _parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="BeH2 16-qubit supernet VQE demo.")
    parser.add_argument("--device", default="cpu", help="torch device (cpu/cuda/npu:0)")
    parser.add_argument("--layers", type=int, default=4)
    parser.add_argument("--supernet-num", type=int, default=6)
    parser.add_argument("--supernet-steps", type=int, default=300)
    parser.add_argument("--ranking-num", type=int, default=120)
    parser.add_argument("--finetune-steps", type=int, default=500)
    parser.add_argument("--seed", type=int, default=7)
    parser.add_argument(
        "--skip-exact",
        action="store_true",
        help="Skip exact dense diagonalization (recommended for 16 qubits).",
    )
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> None:
    args = _parse_args(argv)

    print("BeH2 fixed active-space Hamiltonian")
    print("Geometry (angstrom):")
    for atom, coords in BEH2_GEOMETRY_ANGSTROM:
        x, y, z = coords
        print(f"  {atom:>2s}: ({x:+.6f}, {y:+.6f}, {z:+.6f})")
    print(f"Basis : {BEH2_BASIS}")
    print(f"Charge: {BEH2_CHARGE}")
    print(f"Spin  : {BEH2_SPIN}")
    print(
        "Active space: "
        f"{BEH2_ACTIVE_ELECTRONS} electrons, {BEH2_ACTIVE_SPATIAL_ORBITALS} spatial orbitals"
    )
    print(f"Mapper: {BEH2_QUBIT_MAPPER}")

    t0 = time.time()
    hamiltonian = build_beh2_hamiltonian()
    t1 = time.time()

    terms = hamiltonian_pauli_terms(hamiltonian)
    print(f"\nQubits: {hamiltonian.n_qubits}")
    print(f"Terms : {len(terms)}")
    print(f"Hamiltonian load time: {t1 - t0:.1f}s")

    if not args.skip_exact and hamiltonian.n_qubits <= 12:
        exact = exact_ground_energy(hamiltonian)
        print(f"Dense-matrix exact ground energy: {exact:+.10f}")
    else:
        exact = None
        print("Dense-matrix exact ground energy: skipped")

    kwargs = beh2_vqe_qas_kwargs()
    kwargs.update(
        {
            "layers": args.layers,
            "supernet_num": args.supernet_num,
            "supernet_steps": args.supernet_steps,
            "ranking_num": args.ranking_num,
            "finetune_steps": args.finetune_steps,
            "seed": args.seed,
            "device": args.device,
        }
    )

    print("\nSearching BeH2 ground-state circuit with supernet (task='vqe')...")
    start = time.time()
    result = supernet_qas(hamiltonian, **kwargs)
    elapsed = time.time() - start

    metrics = result.final_metrics
    qas_energy = float(metrics["fine_tuned_energy"])
    baseline_energy = float(metrics["baseline_vqe_energy"])
    print(f"QAS fine-tuned energy     : {qas_energy:+.10f}")
    print(f"fixed-ansatz VQE baseline : {baseline_energy:+.10f}")
    if exact is not None:
        print(f"|QAS - exact|             : {abs(qas_energy - exact):.3e} Ha")
    print(
        "selected CNOT / 2-qubit   : "
        f"{metrics['selected_cnot_count']} / {metrics['selected_two_qubit_count']}"
    )
    print(f"wall-clock time           : {elapsed:.1f}s")

    qasm_path = Path(__file__).with_name("BeH2_cir.qasm")
    save_circuit_qasm3(result.best_circuit, qasm_path)
    print(f"OpenQASM 3.0 saved to: {qasm_path}")

    py_path = Path(__file__).with_name("BeH2_cir.py")
    save_circuit_python(result.best_circuit, py_path)
    print(f"Python circuit saved to: {py_path}")


if __name__ == "__main__":
    main()
